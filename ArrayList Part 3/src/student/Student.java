package student;

public class Student {
	
	public int ID;
	public String first;
	public String last;
	public String email;
	public double score;
	
	public Student(int ID, String first, String last, String email, double score) {
		this.ID = ID;
		this.first = first;
		this.last = last;
		this.email = email;
		this.score = score;
	}
	
}
